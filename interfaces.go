package rdf4go

////////////////////////////////////////////////////////////////////////////////
// type Term
////////////////////////////////////////////////////////////////////////////////

// Term ...
type Term interface {
	// String ...
	ToString() string
}

////////////////////////////////////////////////////////////////////////////////
// type Subject
////////////////////////////////////////////////////////////////////////////////

// Subject ...
type Subject interface {
	Term
	isSubjectType()
}

////////////////////////////////////////////////////////////////////////////////
// type Predicate
////////////////////////////////////////////////////////////////////////////////

// Predicate ...
type Predicate interface {
	Term
	isPredicateType()
}

////////////////////////////////////////////////////////////////////////////////
// type Object
////////////////////////////////////////////////////////////////////////////////

// Object ...
type Object interface {
	Term
	isObjectType()
}

////////////////////////////////////////////////////////////////////////////////
// type Blank
////////////////////////////////////////////////////////////////////////////////

// Blank ...
type Blank interface {
	isSubjectType()
	isObjectType()
	Id() string
	String() string
}

////////////////////////////////////////////////////////////////////////////////
// type IRI
////////////////////////////////////////////////////////////////////////////////

// IRI ...
type IRI interface {
	isSubjectType()
	isPredicateType()
	isObjectType()
	Value() string
	String() string
}

////////////////////////////////////////////////////////////////////////////////
// type Literal
////////////////////////////////////////////////////////////////////////////////

// Literal ...
type Literal interface {
	isObjectType()
	String() string
	Lang() *string
	DataType() IRI
}

////////////////////////////////////////////////////////////////////////////////
// type Triple
////////////////////////////////////////////////////////////////////////////////

// Triple ...
type Triple interface {
	Subject() Subject
	Predicate() Predicate
	Object() Object
}
